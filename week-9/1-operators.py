# 2. Comparison Operators

#! Equal (==)

print(firstNumber == secondNumber) # False

#! Not Equal (!=)

print(firstNumber != secondNumber) # True

#! Greater Than (>)

print(firstNumber > secondNumber) # False

#! Less Than (<)

print(firstNumber < secondNumber) # True

#! Greater Than or Equal To (>=)

print(firstNumber >= secondNumber) # False

#! Less Than or Equal To (<=)

print(firstNumber <= secondNumber) # True
