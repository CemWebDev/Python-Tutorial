
# 1. Arithmetic Operators

firstNumber = 2
secondNumber = 10
thirdNumber = 3

#! Addition (+)

print(firstNumber + secondNumber) # 12

#! Subtraction (-)

print(firstNumber - secondNumber) # -8

#! Multiplicaiton (*)

print(firstNumber * secondNumber) # 20

#! Division (/)

print(firstNumber / secondNumber) # 5.0

#! Modulus (%)

print(firstNumber % thirdNumber) # 3

