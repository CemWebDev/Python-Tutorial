# if statement

firstNumber = 10

if firstNumber > 5:
    print(str(firstNumber) + " is greater than 5")


# if-else syntax
secondNumber = 2

if secondNumber > 5:
    print(str(secondNumber) + " is greater than 5")
else: 
    print(str(secondNumber) + " is not greater than 5")

# if-elif-else syntax

thirdNumber = 6

if thirdNumber > 10:
    print(str(thirdNumber) + " is greater than 10")
elif thirdNumber > 5:
    print(str(thirdNumber) + " is greater than 5")
else:
    print(str(thirdNumber) + " is 5 or less")